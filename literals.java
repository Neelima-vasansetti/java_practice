class literal
{
    public static void main(String[] args) {
        //literals

        double num1 = 0b101;
        System.out.println(num1);
        int num2 = 0x7E;
        System.out.println(num2);
        int num3 = 10_00_00_000;
        System.out.println(num3);
        char c = 'n';
        System.out.println(c);
        c++;
    }
}